        '  performerRef: tool.schema.string().optional(),',
        '  performerName: tool.schema.string().optional(),',
        '}',
        'const actionSchema = tool.schema.discriminatedUnion("type", [',
        '  tool.schema.object({ type: tool.schema.literal("installRegistryAsset"), urn: tool.schema.string(), scope: tool.schema.enum(["global", "stage"]).optional() }),',
        '  tool.schema.object({ type: tool.schema.literal("addDanceFromGitHub"), source: tool.schema.string(), scope: tool.schema.enum(["global", "stage"]).optional() }),',
        '  tool.schema.object({ type: tool.schema.literal("importInstalledPerformer"), urn: tool.schema.string().optional(), performerName: tool.schema.string().optional() }),',
        '  tool.schema.object({ type: tool.schema.literal("importInstalledAct"), urn: tool.schema.string().optional(), actName: tool.schema.string().optional() }),',
        '  tool.schema.object({ type: tool.schema.literal("createTalDraft"), ref: tool.schema.string().optional(), name: tool.schema.string(), content: tool.schema.string(), slug: tool.schema.string().optional(), description: tool.schema.string().optional(), tags: optionalStringArray, openEditor: tool.schema.boolean().optional() }),',
        '  tool.schema.object({ type: tool.schema.literal("updateTalDraft"), draftId: tool.schema.string().optional(), draftRef: tool.schema.string().optional(), draftName: tool.schema.string().optional(), name: tool.schema.string().optional(), content: tool.schema.string().optional(), description: tool.schema.string().optional(), tags: optionalStringArray }),',
        '  tool.schema.object({ type: tool.schema.literal("deleteTalDraft"), draftId: tool.schema.string().optional(), draftRef: tool.schema.string().optional(), draftName: tool.schema.string().optional() }),',
        '  tool.schema.object({ type: tool.schema.literal("createDanceDraft"), ref: tool.schema.string().optional(), name: tool.schema.string(), content: tool.schema.string(), slug: tool.schema.string().optional(), description: tool.schema.string().optional(), tags: optionalStringArray, openEditor: tool.schema.boolean().optional() }),',
