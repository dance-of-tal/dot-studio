        '  messagesFromPerformerIds: optionalStringArray,',
        '  messagesFromPerformerRefs: optionalStringArray,',
        '  messagesFromPerformerNames: optionalStringArray,',
        '  messageTags: optionalStringArray,',
        '  callboardKeys: optionalStringArray,',
        '  eventTypes: tool.schema.array(tool.schema.literal("runtime.idle")).optional(),',
