        '  maxBoardUpdatesPerKey: tool.schema.number().finite().nonnegative().optional(),',
        '  quietWindowMs: tool.schema.number().finite().nonnegative().optional(),',
        '  threadTimeoutMs: tool.schema.number().finite().nonnegative().optional(),',
        '  loopDetectionThreshold: tool.schema.number().finite().nonnegative().optional(),',
        '})',
        'const relationBlueprintSchema = tool.schema.object({',
        '  sourceParticipantKey: tool.schema.string().optional(),',
        '  sourcePerformerId: tool.schema.string().optional(),',
        '  sourcePerformerRef: tool.schema.string().optional(),',
        '  sourcePerformerName: tool.schema.string().optional(),',
        '  targetParticipantKey: tool.schema.string().optional(),',
        '  targetPerformerId: tool.schema.string().optional(),',
        '  targetPerformerRef: tool.schema.string().optional(),',
        '  targetPerformerName: tool.schema.string().optional(),',
        '  direction: tool.schema.enum(["both", "one-way"]).optional(),',
        '  name: tool.schema.string(),',
        '  description: tool.schema.string(),',
        '})',
        'const performerFieldsSchema = {',
        '  model: modelBlueprintSchema.nullable().optional(),',
        '  modelVariant: tool.schema.string().nullable().optional(),',
        '  description: tool.schema.string().nullable().optional(),',
        '  talUrn: tool.schema.string().nullable().optional(),',
        '  talDraftId: tool.schema.string().optional(),',
        '  talDraftRef: tool.schema.string().optional(),',
        '  talDraft: draftBlueprintSchema.optional(),',
        '  addDanceUrns: optionalStringArray,',
        '  addDanceDraftIds: optionalStringArray,',
        '  addDanceDraftRefs: optionalStringArray,',
        '  addDanceDrafts: tool.schema.array(draftBlueprintSchema).optional(),',
        '  removeDanceUrns: optionalStringArray,',
        '  removeDanceDraftIds: optionalStringArray,',
        '  addMcpServerNames: optionalStringArray,',
        '  removeMcpServerNames: optionalStringArray,',
        '}',
