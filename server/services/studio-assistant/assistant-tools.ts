        '  tool.schema.object({ type: tool.schema.literal("deleteDanceDraft"), draftId: tool.schema.string().optional(), draftRef: tool.schema.string().optional(), draftName: tool.schema.string().optional() }),',
        '  tool.schema.object({ type: tool.schema.literal("upsertDanceBundleFile"), draftId: tool.schema.string().optional(), draftRef: tool.schema.string().optional(), draftName: tool.schema.string().optional(), path: tool.schema.string().refine((value) => normalizeAssistantBundlePath(value) !== null, "Bundle path must stay within the draft bundle."), content: tool.schema.string() }),',
